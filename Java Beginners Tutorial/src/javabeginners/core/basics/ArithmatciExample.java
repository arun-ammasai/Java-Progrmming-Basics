package javabeginners.core.basics;

public class ArithmatciExample {

	public static void main(String[] args) {
		int num1 = 100;
		int num2 = 50;
		System.out.println("Add :"+(num1+num2));// 150
		System.out.println("Sub :"+(num1-num2));// 50
		System.out.println("Mul :"+(num1*num2));// 5000
		System.out.println("Div :"+(num1/num2));// 2
		System.out.println("Mod :"+(num1%num2));// 0
		
	}

}
